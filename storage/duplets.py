def normalize(text):
    # Lexical exceptions
    known = {
        "Stelei": "Steaua",
        "Unirii": "Unirea",
        "Rapidului": "Rapid",
        # ... add more
    }
    if text in known:
        return known[text]

    # Morphological rules
    if text.endswith("ului"):
        return text[:-4]
    if text.endswith("ei"):
        return text[:-2] + "a"
    if text.endswith("ilor"):
        return text[:-4]

    # Fallback to T5
    # return t5_normalize(text)
    return text


